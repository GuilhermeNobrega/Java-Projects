public class Main {
  public static void main(String[] args) {
    System.out.println("Hello World");
  }
}

// Isso define uma classe chamada "Main".
public class Main {
  // Aqui, você está declarando um método chamado "main".
  // O método "main" é o ponto de entrada para um programa Java.
  public static void main(String[] args) {
    // Esta linha imprime "Hello World" no console.
    // "System.out" é um objeto que representa a saída padrão (normalmente o console).
    System.out.println("Hello World"); // O método "println" imprime uma linha de texto.
  } // Isso fecha o método "main".
} // Isso fecha a classe "Main".

